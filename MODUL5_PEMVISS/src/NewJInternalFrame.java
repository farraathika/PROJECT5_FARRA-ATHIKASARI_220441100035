    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        try{
            Statement statement = conn.getConnection().createStatement();
            int updatedRows = statement.executeUpdate("UPDATE pemesanan SET pembeli = '" + pembeli.getText()
                    + "', nama_menu='" + namaMenu.getSelectedItem()
                    + "', harga='" + harga.getText() + "', jumlah='" + jumlah.getText()
                    + "', subtotal='" + subtotal.getText() + "', tunai='" + tunai.getText()
                    + "', kembalian='" + kembalian.getText() + "' WHERE kode='" + kode.getText() + "'");
            statement.close();
            
            if (updatedRows > 0){
                JOptionPane.showMessageDialog(this, "Data berhasil diubah");
                bersihkan();
                tampilData();
            }else{
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan");
            }
        }catch (Exception t){
            System.out.println(t.getMessage());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

