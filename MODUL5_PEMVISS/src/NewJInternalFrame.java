    public void tampilData(){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("PEMBELI");
        tbl.addColumn("NAMA MENU");
        tbl.addColumn("KODE");
        tbl.addColumn("HARGA");
        tbl.addColumn("JUMLAH");
        tbl.addColumn("SUBTOTAL");
        tbl.addColumn("TUNAI");
        tbl.addColumn("KEMBALIAN");
        
        tabelpenjualan.setModel(tbl); // tbl sesuaikan dengan variable pada table
        try {
            Statement st = conn.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM pemesanan");
            while (rs.next()) {
                tbl.addRow( new Object[] {
                    rs.getString("pembeli"),
                    rs.getString("nama_menu"),
                    rs.getString("kode"),
                    rs.getString("harga"),
                    rs.getString("jumlah"),
                    rs.getString("subtotal"),
                    rs.getString("tunai"),
                    rs.getString("kembalian")
                });
                tabelpenjualan.setModel(tbl);
            }
        } catch (Exception e) {
        }
    }
